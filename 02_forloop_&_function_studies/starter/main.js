function getPizzasNames(nameArg, tableau){
    let pizzasNames = [];

    for (let i = 0; i < tableau.length; i++) {
        const tabPizza = tableau[i];

        for (let j = 0; j < tabPizza.ingredients.length; j++) {

            if(tabPizza.ingredients[j].includes(nameArg)) {

                pizzasNames.push(tabPizza.name)
            }
        }
    }
    return pizzasNames;
}



